"""(!=)Not Equal Operator Check The X And Y If Both Are Not Same Value It's Asign True In C Other wise Asign False"""
C=X!=Y
print(C)

"""(>)GreterThan Operator Check X If X Is Greterthan Y It Asign True In C Otherwise False"""
