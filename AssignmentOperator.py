#Asign Value To M And N
M=20
N=30

"""(=)Equal Operator Asign The Value From Right Side Operand To Left Side Oparand"""
Q=M+N
print(Q)

"""(+=)Plus Equal Operator Add The Value In Right Side Operand From Left Side Oparand"""
Q+=M
print(Q)

"""(-=)Minus Equal Operator Subtract The Left Side Oparand Value From Right Side Oparand  Value"""
Q-=M
print(Q)

"""(*=)Multiply Equal Operator Multiply Right Side Oparand To Left Side Oparand"""
Q*=M
print(Q)

"""(/=)Devide Equal Operator Devide Right Side Oparand To Left Side Oparand"""
Q/=M
print(Q)

"""(%=)Modulus Equal Operator Module The Right Side Oparand To Left Side Oparand"""
Q%=M
print(Q)
