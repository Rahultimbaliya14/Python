#Asign Value To J And K
J=True
K=False

"""(and)And Keyword(Operator) Return True If Both Operand True Else False"""
L=K and J
print(L)

"""(or)Or Keyword(Operator) Return True If Either One Operand True Else False"""
L=J or K
print(L)

"""(not)Not Keyword(operator) Complement The Result If Result Is True It's Return False And Result Will False It Return True"""

L=not(J and K)
print(L)